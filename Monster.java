public class Monster
{
    int hp = 0;
    int attack1 = 0;
    int attack2 = 0;
    int loot = 0;
    int speed = 0;

    public Monster(int hp, int attack1, int attack2, int loot, int speed)
    {
        this.hp = hp;
        this.attack1 = attack1;
        this.attack2 = attack2;
        this.loot = loot;
        this.speed = speed;
    }
    
}
